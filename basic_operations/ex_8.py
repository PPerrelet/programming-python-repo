# To what value does the following expression evaluate?

print('12' < '9')

# This prints true because we are comparing 'strings' and the ASCII code value of 9 is greater than 12
# Python compares the value of the of the strings by the first character in the string.
# 1 < 9 
# 49 < 57

# You can convert these to integers if you wish to compare as such.

print(int('12') < int('9'))